def format_log_entry(when: datetime, author: str, summary: str, task_title: str | None = None) -> str:
    line = f"- {when:%H:%M} · {author} — {summary.strip()}"
    if task_title:
        line += f" ([[task: {task_title}]])"
    return line


def _insert_entry(existing: str, heading: str, entry: str) -> str:
    """Newest day section on top; newest entry on top within its day."""
    if heading in existing:
        lines = existing.split("\n")
        idx = lines.index(heading)
        lines.insert(idx + 1, entry)
        return "\n".join(lines)
    block = f"{heading}\n{entry}\n"
    if existing.strip():
        return f"{block}\n{existing.lstrip()}"
    return f"# Project log\n\n{block}"


def append_log_entry(wiki_root: Path, when: datetime, author: str, summary: str, task_title: str | None = None) -> None:
    log = Path(wiki_root) / "log.md"
    existing = log.read_text(encoding="utf-8") if log.exists() else ""
    new_text = _insert_entry(existing, f"## {when:%Y-%m-%d}", format_log_entry(when, author, summary, task_title))
    log.parent.mkdir(parents=True, exist_ok=True)
    log.write_text(new_text, encoding="utf-8")


def _note_title(text: str, path: Path) -> str:
    """First `# ` heading, else the filename humanized."""
    for line in text.splitlines():
        if line.startswith("# "):
            return line[2:].strip()
    return path.stem.replace("-", " ").replace("_", " ").strip()


def _note_summary(text: str) -> str:
    """One line for the catalog: frontmatter `description:` if present, else the
    first non-blank, non-heading line. Truncated."""
    text = text.replace("\r\n", "\n")
    fm = re.match(r"^---\n(.*?)\n---\n", text, re.DOTALL)
    body = text
    if fm:
        m = re.search(r"^description:\s*(.+)$", fm.group(1), re.MULTILINE)
        if m:
            return m.group(1).strip().strip("\"'")[:140]
        # Frontmatter present but no description: skip the YAML block before
        # scanning, else its body lines (tags:, status:, …) leak in as the summary.
        body = text[fm.end():]
    for line in body.splitlines():
        s = line.strip()
        if not s or s.startswith("#") or s.startswith("---"):
            continue
        return s[:140]
    return ""


def rebuild_index(wiki_root: Path) -> None:
    """Regenerate wiki/index.md — a catalog (title + one-line summary) of the
    project's notes so an agent can see what exists without reading every file.
    Excludes index.md, log.md, and the graphify-out/ build dir. No-op if the wiki
    directory does not exist."""
    root = Path(wiki_root)
    if not root.is_dir():
        return
    entries: list[tuple[str, str, str]] = []
    for md in sorted(root.rglob("*.md")):
        rel = md.relative_to(root).as_posix()
        if rel in ("index.md", "log.md") or rel.endswith("/log.md") or rel.startswith("graphify-out/"):
            continue
        try:
            text = md.read_text(encoding="utf-8")
        except OSError:
            continue
        entries.append((rel, _note_title(text, md), _note_summary(text)))
    body = ["# Wiki index", "", "<!-- Auto-generated by Proxima. Catalog of this project's notes. -->", ""]
    if entries:
        for rel, title, summary in entries:
            body.append(f"- [{title}]({rel})" + (f" — {summary}" if summary else ""))
    else:
        body.append("_No notes yet._")
    (root / "index.md").write_text("\n".join(body) + "\n", encoding="utf-8")


