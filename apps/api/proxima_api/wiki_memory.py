def list_project_designs(project_root: Path) -> list[dict[str, Any]]:
    """Scan the project's Design Studio designs (artifacts/design/*/scene.json) so the
    MAIN chat can be made aware of visuals created/edited in the separate design chat.
    Best-effort + cheap; newest first, capped."""
    out: list[dict[str, Any]] = []
    ddir = project_root / "artifacts" / "design"
    if not ddir.is_dir():
        return out
    try:
        entries = sorted(ddir.iterdir())
    except OSError:
        return out
    for d in entries:
        scene = d / "scene.json"
        if not scene.is_file():
            continue
        try:
            s = json.loads(scene.read_text(encoding="utf-8", errors="ignore"))
            out.append({"id": str(s.get("id") or d.name), "title": str(s.get("title") or d.name), "mtime": scene.stat().st_mtime})
        except Exception:
            continue
    out.sort(key=lambda x: x["mtime"], reverse=True)
    return out[:12]


